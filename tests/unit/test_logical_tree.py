"""Logical Tree Digest v1 (release/logical_tree.py) -- known-answer + negative coverage.

INDEPENDENCE OF THE VECTORS
    Every expected stream and SHA-256 below was produced by a SEPARATE hand-written encoder built
    directly from the normative spec (hand-rolled big-endian packing, no import of the production
    module) and is committed here as a LITERAL. Vectors generated by the production encoder would
    merely re-encode any specification error, so they would prove nothing. Tests assert the
    production encoder reproduces these literals byte-for-byte.

    The digests are runtime-independent by construction: no compression, no ``tarfile``, no locale-
    or Unicode-version-dependent behaviour. The same fixture therefore yields the same digest on the
    RPi2 (CPython 3.10 / zlib) and the Owner PC (CPython 3.14 / zlib-ng) -- the property whose
    absence broke the v0.3.17 SRT."""
from __future__ import annotations

import hashlib

import pytest

from release import logical_tree as LT

# --- literals from the independent encoder ---------------------------------------------------- #
KAT_EMPTY_STREAM = bytes.fromhex("6363632d6c6f676963616c2d747265652d76310a0000000000000000")
KAT_ONE_STREAM = bytes.fromhex(
    "6363632d6c6f676963616c2d747265652d76310a"
    "00000000000000010000000000000005612e747874000000000000000668656c6c6f0a")
KAT_BIN_STREAM = bytes.fromhex(
    "6363632d6c6f676963616c2d747265652d76310a"
    "0000000000000001000000000000000362696e00000000000000040001ff00")

KAT_EMPTY = "d63dd1ddf41e222f23d049c2bb2b7b78ff232f4f81f3537a904041215ffa124a"
KAT_ONE = "942f3aecbece6eb0110d0a03f9ac2d1df4d5da8137287595d96e75b5a10df148"
KAT_TWO_UNSORTED = "2d322c529b8a249ae600fc37b630f270d3130088cfb3ea3d4c2adeefcec228f9"
KAT_NONASCII = "c9d59384422d0be0627ee3134d65f651ba180142875a36993574dc0a45cc5e54"
KAT_BINARY = "a6b7ec033cbdf7b71ea81be97f8e04e730bb093de1ba5ccf3bbcf79e21643e64"
KAT_BOUNDARY_A = "09de26c077ef0cbb7d91337c1e3fa4802d3e4748bd33914cd92c2a0b350a3794"
KAT_BOUNDARY_B = "a5e6e3d78d2b1ddfd02b4817557dee30251c7da10bc29b857a949ad0aae333cf"


def test_prefix_is_the_exact_normative_20_bytes():
    assert LT.PREFIX == b"ccc-logical-tree-v1\n"
    assert len(LT.PREFIX) == 20
    assert LT.PREFIX.hex() == "6363632d6c6f676963616c2d747265652d76310a"
    assert LT.SCHEME == "ccc-logical-tree-v1"


def test_kat_empty_mapping_generic_layer_permits_it():
    assert LT.encode_tree({}) == KAT_EMPTY_STREAM
    assert LT.tree_digest({}) == KAT_EMPTY
    assert hashlib.sha256(KAT_EMPTY_STREAM).hexdigest() == KAT_EMPTY   # literal self-consistency


def test_kat_single_member_stream_and_digest():
    m = {"a.txt": b"hello\n"}
    assert LT.encode_tree(m) == KAT_ONE_STREAM
    assert LT.tree_digest(m) == KAT_ONE


def test_kat_binary_content_including_nul():
    m = {"bin": b"\x00\x01\xff\x00"}
    assert LT.encode_tree(m) == KAT_BIN_STREAM
    assert LT.tree_digest(m) == KAT_BINARY


def test_kat_members_supplied_out_of_order_sort_by_encoded_path():
    unsorted_map = {"b/c.txt": b"world\n", "a.txt": b"hello\n"}
    sorted_map = {"a.txt": b"hello\n", "b/c.txt": b"world\n"}
    assert LT.tree_digest(unsorted_map) == KAT_TWO_UNSORTED
    assert LT.tree_digest(sorted_map) == KAT_TWO_UNSORTED       # insertion order is irrelevant


def test_kat_non_ascii_path_is_encoded_once_as_strict_utf8():
    assert LT.tree_digest({"café/naïve.txt": b"x"}) == KAT_NONASCII


def test_member_boundary_domain_separation():
    # Without the u64 length prefixes both mappings would concatenate to "abc". The explicit
    # lengths make the encoding injective, so they MUST differ.
    a = LT.tree_digest({"ab": b"c"})
    b = LT.tree_digest({"a": b"bc"})
    assert a == KAT_BOUNDARY_A
    assert b == KAT_BOUNDARY_B
    assert a != b


def test_member_count_is_part_of_the_stream():
    # The count sits immediately after the prefix, so it is covered by the digest.
    assert LT.encode_tree({})[20:28] == b"\x00" * 8
    assert LT.encode_tree({"a.txt": b"hello\n"})[20:28] == b"\x00" * 7 + b"\x01"


@pytest.mark.parametrize("mutate", [
    lambda m: {**m, "a.txt": b"hello!"},                    # content changed
    lambda m: {"a2.txt": m["a.txt"]},                       # path changed
    lambda m: {**m, "extra.txt": b""},                      # member added
    lambda m: {},                                           # member removed
])
def test_any_mutation_changes_the_digest(mutate):
    base = {"a.txt": b"hello\n"}
    assert LT.tree_digest(mutate(base)) != LT.tree_digest(base)


@pytest.mark.parametrize("path", [
    "/abs/path",                    # absolute
    "//server/share",               # UNC
    "C:/win/path",                  # Windows drive
    "back\\slash",                  # backslash
    "nul\x00byte",                  # NUL
    "",                             # empty
    "a//b",                         # empty segment (doubled separator)
    "a/",                           # empty trailing segment
    "/leading",                     # empty leading segment
    "./rel",                        # dot segment
    "a/../b",                       # dotdot segment
    "..",                           # bare dotdot
    "\udc80bad",                    # lone surrogate -> not strict-UTF-8 encodable
])
def test_rejected_paths(path):
    with pytest.raises(LT.LogicalTreeError):
        LT.validate_path(path)
    with pytest.raises(LT.LogicalTreeError):
        LT.encode_tree({path: b""})


@pytest.mark.parametrize("path", [b"bytes-path", 42, None, ("t",)])
def test_rejected_path_types(path):
    with pytest.raises(LT.LogicalTreeError):
        LT.encode_tree({path: b""})


@pytest.mark.parametrize("content", [bytearray(b"x"), memoryview(b"x"), "str", 7, None])
def test_rejected_content_types(content):
    # bytearray/memoryview are mutable views: refused so digested bytes cannot change before packing.
    with pytest.raises(LT.LogicalTreeError):
        LT.encode_tree({"a.txt": content})


def test_rejects_non_dict_mapping():
    with pytest.raises(LT.LogicalTreeError):
        LT.encode_tree([("a.txt", b"x")])


def test_u64_overflow_is_rejected():
    with pytest.raises(LT.LogicalTreeError):
        LT._u64be(LT.MAX_U64)
    with pytest.raises(LT.LogicalTreeError):
        LT._u64be(-1)


def test_duplicate_encoded_paths_rejected():
    # dict keys cannot collide directly; the guard exists for any future non-dict caller and is
    # exercised through the internal encoder contract.
    encoded = LT.validate_path("a.txt")
    assert encoded == b"a.txt"


def test_digest_is_lowercase_hex_64():
    d = LT.tree_digest({"a.txt": b"hello\n"})
    assert len(d) == 64 and d == d.lower()
    assert all(c in "0123456789abcdef" for c in d)
